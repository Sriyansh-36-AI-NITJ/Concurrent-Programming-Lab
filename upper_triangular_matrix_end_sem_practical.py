# Cell 6: Allocate memory and run the kernel

# Transfer input from CuPy to NumPy for Numba
A_cpu = cp.asnumpy(A_gpu)

# Allocate pinned device arrays for Numba
A_device      = cuda.to_device(A_cpu)
result_device = cuda.device_array((N, N), dtype=np.int32)

# ─── LAUNCH KERNEL ───────────────────────────────────────────
upper_triangular_kernel[blocks_per_grid, threads_per_block](
    A_device, result_device, N
)

# Synchronize GPU before reading results
