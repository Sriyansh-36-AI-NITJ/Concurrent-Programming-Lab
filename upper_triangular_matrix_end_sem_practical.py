# Cell 4: Write the CUDA kernel using Numba
# Each thread handles ONE cell → checks if col >= row

from numba import cuda
import numpy as np
import math

@cuda.jit
def upper_triangular_kernel(A, result, n):
    """
    CUDA kernel: each thread processes ONE cell (row, col).

    Thread mapping:
      - blockIdx.x  → which block along X (column direction)
      - blockIdx.y  → which block along Y (row direction)
      - threadIdx.x → thread column within block
      - threadIdx.y → thread row within block
    """
    # Compute global row and column index for THIS thread
    row = cuda.blockIdx.y * cuda.blockDim.y + cuda.threadIdx.y
    col = cuda.blockIdx.x * cuda.blockDim.x + cuda.threadIdx.x

    # Boundary check: ensure thread maps to a valid cell
    if row < n and col < n:
        if col >= row:
            # Upper triangle (including diagonal): keep original value
            result[row, col] = A[row, col]
        else:
            # Lower triangle: set to zero
            result[row, col] = 0

print("✅ Numba CUDA kernel compiled successfully.")
print("\nKernel logic:")
print("  Thread (row, col) → result[row,col] = A[row,col] if col >= row else 0")

# Cell 5: Configure CUDA thread blocks and grid dimensions

N = 1024

# Block size: 32×32 = 1024 threads per block (hardware maximum)
BLOCK_DIM_X = 32
BLOCK_DIM_Y = 32
threads_per_block = (BLOCK_DIM_X, BLOCK_DIM_Y)

# Grid size: ceil(N / BLOCK_DIM) blocks in each dimension
GRID_DIM_X = math.ceil(N / BLOCK_DIM_X)   # = 32
GRID_DIM_Y = math.ceil(N / BLOCK_DIM_Y)   # = 32
blocks_per_grid = (GRID_DIM_X, GRID_DIM_Y)

total_threads = (GRID_DIM_X * BLOCK_DIM_X) * (GRID_DIM_Y * BLOCK_DIM_Y)

