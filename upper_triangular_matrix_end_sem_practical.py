# Cell 8: Benchmark CUDA vs NumPy vs CuPy

import time

WARMUP = 5
RUNS   = 50

# ── Warmup ──────────────────────────────────────────
for _ in range(WARMUP):
    upper_triangular_kernel[blocks_per_grid, threads_per_block](
        A_device, result_device, N
    )
    cuda.synchronize()

# ── CUDA (Numba kernel) ──────────────────────────────
t0 = time.perf_counter()
for _ in range(RUNS):
    upper_triangular_kernel[blocks_per_grid, threads_per_block](
        A_device, result_device, N
    )
    cuda.synchronize()
cuda_time = (time.perf_counter() - t0) / RUNS * 1000

# ── CuPy triu (GPU, optimized library) ──────────────
cp.cuda.Stream.null.synchronize()
t0 = time.perf_counter()
for _ in range(RUNS):
    R = cp.triu(A_gpu)
    cp.cuda.Stream.null.synchronize()
cupy_time = (time.perf_counter() - t0) / RUNS * 1000

# ── NumPy triu (CPU) ─────────────────────────────────
t0 = time.perf_counter()
for _ in range(RUNS):
    _ = np.triu(A_cpu)
